package com.mycompany.longitudcircunferencia;

/**
 *
 * @author miAsus
 */
public class LongitudCircunferencia {
    
    public static void main(String[] args) {
        // Definir el radio de la circunferencia
        double radio = 3.0; // En metros

        // Calcular la longitud de la circunferencia
        double longitudCircunferencia = 2 * Math.PI * radio;

        // Imprimir el resultado
        System.out.println("La longitud de la circunferencia con radio de 3 metros es: " + longitudCircunferencia + " metros");
    }
}
